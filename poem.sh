# Ode to emacs
[ -o emacs ]; : D

# Conviction
time for reflection in silence
do [ -u bt ]
   set tles in;
   test -s "my convictions";
   make -s me
   [ questi -o -n everything ]
   : "I've" ever
done;
[ n -ot an ]
echo chamber
